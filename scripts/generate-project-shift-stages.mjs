import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const root = process.cwd();
const stageDirectory = join(root, 'src', 'features', 'project-shift', 'stages');
const output = join(root, 'src', 'features', 'project-shift', 'stage-data.generated.ts');
const files = readdirSync(stageDirectory).filter((file) => /^t\d{2}-\d{2}\.json$/.test(file)).sort();

if (files.length !== 30) {
  throw new Error(`Project SHIFT requires 30 stage JSON files; found ${files.length}.`);
}

const sources = files.map((file) => JSON.parse(readFileSync(join(stageDirectory, file), 'utf8')));
const contents = [
  '// Generated by scripts/generate-project-shift-stages.mjs. Do not edit directly.',
  `export const STAGE_SOURCES = ${JSON.stringify(sources, null, 2)} as const;`,
  ''
].join('\n');

writeFileSync(output, contents, 'utf8');
console.log(`Generated ${files.length} Project SHIFT stages.`);
